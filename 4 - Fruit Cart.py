list1 = {'🍎 apple', '🍌 banana', '🍊 orange', '🍓 strawberries'}
list2 = {'🍒 cherries', '🫐 blueberries', '🍇 grapes', '🍓 strawberries'}

# Union of sets
union_result = list1.union(list2)

# Intersection of sets
intersection_result = list1.intersection(list2)

# Difference of sets
difference_result = list1.difference(list2)

print(union_result)
print(intersection_result)
print(difference_result)

